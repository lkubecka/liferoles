package com.liferoles;

public class LifeRolesDBException extends Exception {
	
	    public LifeRolesDBException(String message) {
	        super(message);
	    }

	    public LifeRolesDBException(Throwable cause) {
	        super(cause);
	    }
	 
	    public LifeRolesDBException(String message, Throwable cause) {
	        super(message, cause);
	    }
	    
}
